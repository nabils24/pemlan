package tugas.codelab1;


/**
 * Class MinFinder - Deskripsi singkat mengenai kelas ini.
 */
public class MinFinder {

    public static int findMin(int a, int b, int c) {
        if (a <= b && a <= c) {
            return a;
        } else if (b <= a && b <= c) {
            return b;
        } else {
            return c;
        }
    }
}
